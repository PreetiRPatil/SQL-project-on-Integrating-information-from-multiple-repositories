CREATE TABLE CUSTOMERS_TBL (CUST_ID NUMBER, CUST_NAME VARCHAR2(255), ADDRESS VARCHAR2(2000), PH_NO NUMBER(10), CONSTRAINT CUST_ID_pk PRIMARY KEY (CUST_ID));
CREATE TABLE PURCHASES_TBL (PURCHASE_ID NUMBER, PRODUCT_ID NUMBER, CUST_ID NUMBER, QUANTITY NUMBER, AMOUNT NUMBER, SALE_DATE DATE, CONSTRAINT PURCHASE_ID_pk PRIMARY KEY (PURCHASE_ID), CONSTRAINT fk_PRODUCT
    FOREIGN KEY (PRODUCT_ID)
