CUST_ID number,
CUST_NAME varchar2(100),
ADDR varchar2(50),
PH_NUMBER NUMBER,
primary key(CUST_ID) 
);

create table PRD(
PRD_ID number,
PRD_TYPE varchar2(100),
PRD_NAME varchar2(100),
PRD_QUANTITY number,
primary key(PRD_ID)
);

create table Ord_tbl(
ORDER_ID number,
cust_id int,
ORDER_DATE date,
sales_tax float,
shipping float,
order_status varchar2(100),
primary key(order_id)
);

create table sales(
sale_id number,
order_id number,
PRD_ID number,
sale_description varchar2(100),
item_quantity number,
price_per_unit number,
primary key(sale_id),
foreign key(order_id) references Ord_tbl(order_id),
foreign key(PRD_ID) references PRD(PRD_ID )
);
