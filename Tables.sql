CREATE TABLE CATEGORY_TAB (CATEGORY_ID NUMBER, CATEGORY VARCHAR2(255), CONSTRAINT CATEGORY_ID_pk PRIMARY KEY (CATEGORY_ID)); 

CREATE TABLE GENRE_TAB (GENRE_ID NUMBER, GENRE VARCHAR2(255), CONSTRAINT GENRE_pk PRIMARY KEY (GENRE_ID)); --   (CLASSICAL, JAZZ, POP, HIP HOP, ROCK, RAP, FOLK, COUNTRY)

CREATE TABLE PRODUCT_TAB (PRODUCT_ID NUMBER, PRODUCT_TYPE VARCHAR2(255), TITLE VARCHAR2(255), STOCK_COUNT NUMBER, CONSTRAINT PRODUCT_ID_pk PRIMARY KEY (PRODUCT_ID), CONSTRAINT check_PRODUCT_TYPE CHECK (PRODUCT_TYPE IN ('BOOK', 'MUSIC_CD')), CONSTRAINT TITLE_CNST UNIQUE (TITLE)); 

CREATE TABLE BOOKS_TBL (ID number, ISBN NUMBER, PRODUCT_ID NUMBER, TITLE VARCHAR2(255), AUTHORS VARCHAR2(255), PRICE NUMBER, CATEGORY_ID NUMBER, DESCRIPTION VARCHAR2(2000), 
    CONSTRAINT ID_BK_pk PRIMARY KEY (ID), 
    CONSTRAINT fk_CATEGORY_ID
    FOREIGN KEY (CATEGORY_ID)
    REFERENCES CATEGORY_TAB(CATEGORY_ID),
    CONSTRAINT fk_PRODUCT_BK_ID
    FOREIGN KEY (PRODUCT_ID)
    REFERENCES PRODUCT_TAB(PRODUCT_ID),
    CONSTRAINT fk_PRODUCT_book_Title
    FOREIGN KEY (TITLE)
    REFERENCES PRODUCT_TAB(TITLE),
    CONSTRAINT ISBN_UC UNIQUE (ISBN)); 
    
CREATE TABLE MUSIC_CD_TBL (ID NUMBER, PRODUCT_ID NUMBER, TITLE VARCHAR2(255), ARTISTS VARCHAR2(255), RECORD_COMPANY VARCHAR2(255), PRICE NUMBER, GENRE_ID NUMBER, DESCRIPTION VARCHAR2(2000), 
    CONSTRAINT ID_MSC_pk PRIMARY KEY (ID), CONSTRAINT fk_GENRE_ID
    FOREIGN KEY (GENRE_ID)
    REFERENCES GENRE_TAB(GENRE_ID),
    CONSTRAINT fk_PRODUCT_MSC_ID
    FOREIGN KEY (PRODUCT_ID)
    REFERENCES PRODUCT_TAB(PRODUCT_ID),
    CONSTRAINT fk_PRODUCT_MSC_Title
    FOREIGN KEY (TITLE)
    REFERENCES PRODUCT_TAB(TITLE));     
    
CREATE SEQUENCE BOOKS_TBL_ID_SEQ
 START WITH     1
 INCREMENT BY   1
 NOCACHE
 NOCYCLE;

CREATE SEQUENCE MUSIC_CD_TBL_ID_SEQ
